CREATE TABLE USERS_LOG(
	ID NUMBER(7) NOT NULL,
	USER_ID NUMBER(7) NOT NULL,
	SOURCE VARCHAR2(255 BYTE) NOT NULL,
	ACTION VARCHAR2(255 BYTE) NOT NULL,
	ACTION_TIME DATE NOT NULL,
	PRIMARY KEY (ID)
);

CREATE TABLE USERS_LOGGED(
	ID NUMBER(7) NOT NULL,
	SESSION_ID NUMBER(14) NOT NULL,
	USER_ID    NUMBER(7) NOT NULL,
	USERNAME   VARCHAR2(255 BYTE) NOT NULL,
	USER_ROLE  NUMBER(7) NOT NULL,
	ACTION_TIME DATE NOT NULL,
	PRIMARY KEY (ID)
);

CREATE TABLE METHOD_ACCESS(
	ID NUMBER(7) NOT NULL,
	METHOD_NAME VARCHAR2(255 BYTE) NOT NULL,
	ROLE_ID NUMBER(7) NOT NULL,
	PRIMARY KEY (ID)
);

CREATE TABLE ROLES (
	ID NUMBER(7) NOT NULL,
	ROLE_NAME VARCHAR(64) NOT NULL,
	RIGHT_VALUE NUMBER(7) NOT NULL,
	PRIMARY KEY (ID)
);
CREATE TABLE ACCOUNT (
	ID NUMBER(7) NOT NULL,
	SESSION_ID NUMBER(14),
	LOGIN VARCHAR(64) NOT NULL,
	PASSWORD VARCHAR(64) NOT NULL,
	LOGIN_TIME DATE,
	LOGOUT_TIME DATE,
	ROLE_ID NUMBER(7) NOT NULL REFERENCES ROLES(ID) ON DELETE CASCADE,
	PRIMARY KEY (ID)
);
